package org.opendao.IntelligenceGraph;

public class DatePair {
    private Long start;
    private Long end;

    public DatePair(Long start, Long end) {
        this.start = start;
        this.end = end;
    }

    public Long getStart() {
        return this.start;
    }

    public Long getEnd() {
        return this.end;
    }
}
